#include <stdio.h>
int main()
{
    int a, b;
    printf("Enter two numbers: ");
    scanf("%d %d", &a, &b);
    printf("Addition of given numbers are : %d\n", a + b);
    printf("Subtraction of given numbers are : %d\n", a - b);
    printf("Multiplication of given numbers are : %d\n", a * b);
    printf("Division of given numbers are : %d\n", a / b);
    return 0;
}
